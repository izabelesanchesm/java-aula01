package exemplo03;

public class Exemplo03 {

    //while e for
    public static void main(String[] args) {
        int cont = 0;

        while (cont < 10){
            System.out.print(cont + " ");
            cont++;
        }

        System.out.println();

        for (int i = 0; i < 10; i++){
            System.out.print(i + " ");
        }
    }
}
